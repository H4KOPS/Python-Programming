"""
Syntax of indexing in Python: string[index]
Syntax of slicing in Python: string[start:end:step]
- start: Staring index at which the slicing operation starts
- end: Ending index at which the slicing operation stops (excluded)
- step: Integer that specifies the step for the slicing operation
"""
my_character = "Hello World"
print(my_character[1:12:3])
