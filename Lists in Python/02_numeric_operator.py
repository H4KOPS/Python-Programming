num = [10, 9, 8.3, 5, 8, 1]
# Smallest number in the list.
# min()
print("Smallest Number:",min(num))
print(f"Smallest Number: {min(num)}")
#max()
print("Biggest Number:",max(num))
print(f"Biggest Number: {max(num)}")
# sum()
print("Total Sum of Number:",sum(num))
print(f"Total Sum of Number: {sum(num)}")
